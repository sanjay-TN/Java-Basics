package day2;

public class DataTypes {

	public static void main(String[] args) {
		// data types a) Primitive b)non-primitive or collections
		int a=100,b=100; 
		//Numeric data types
		System.out.println("value of a and b is "+a+" "+b+"\n" +" and their sum is "+(a+b));
		
		//byte
		byte by=123;
		System.out.println(by);
		
		//short
		short s=233;
		System.out.println(s);
		
		//long (literal l or L is needed
		long l=123445676L;
		System.out.println(l);
		
		// decimal numbers : float,double
		float prise=23.4f;
		System.out.println(prise);
		
		double d=23.34522333;
		System.out.println(d);
		
		// character char
		char c='a';
		System.out.println(c);
		
		String s1="ad";
		System.out.println(s1);
		
		// boolean
		boolean b1=true;
		System.out.println(b1);
		
		
		//constant variable
//		final int a1=100;
//		System.out.println(a1);
//		a1=200; cannot be further modified
//		System.out.println(a1);
		

	}

}
